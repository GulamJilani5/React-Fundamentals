/********************* Reactjs 16 vs Reactjs 18

Feature              	           React 16	                               React 18
Rendering Model          	       Synchronous rendering	               Concurrent rendering
Batching of Updates	               Only inside event handlers	           Automatic batching everywhere
useTransition Hook	               Not available	                       Available for deferring updates
useId Hook	                       Not available	                       Available
Server-Side Rendering	           Basic streaming	                       Streaming with Suspense
Suspense	                       Only for lazy loading components        Extended to data fetching
                                   with React.lazy()    
Root API	                       ReactDOM.render()	                  createRoot() API
CSS Insertion Hook	               Not available	                      useInsertionEffect() available
Immediate Updates	               No direct control	                  flushSync() API

***************************/
